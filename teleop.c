/* CONSTANTS */
//MUX Sensors
const tMUXSensor elevatorTouch = msensor_S2_2;

//Joystick threshold
const int THRESHOLD = 20;

/* GLOBALS */
elevatorPositions elevatorPosition;
bool elevatorMoving = false;

/* FUNCTION PROTOTYPES */
void setMotor(mVals *m);
void setMotor(float fl, float fr, float bl, float br);

void setGrabberServo(int val);

void moveElevatorDown();

/* Task Prototypes */
task btnListener();
task elevatorMove();

/* TASKS */
task elevatorMove() {
	servo[armHatch] = ARMHATCHUP;

	switch (elevatorPosition) {
		case elevatorDown:
			moveElevatorDown();
			break;
		default:
			break;
	}
}

task btnListener() {
	bool grabCylinderDown = false;
	bool pressed3 = false;

	bool armServoDown = false;
	bool pressed1 = false;

	while(true) {
		getJoystickSettings(joystick);

		/* JOYSTICK 2 */
		//Elevator Presets
		if (joy2Btn(2)) {
			elevatorPosition = elevatorDown;

			//Stop the elevator task just incase
			StopTask(elevatorMove);

			StartTask(elevatorMove);
		}

		//Elevator
		if (!elevatorMoving) {
			if (joy1Btn(7))
				motor[elevator] = -100;
			else if (joy1Btn(8))
				motor[elevator] = 100;
			else
				motor[elevator] = 0;
		}

		if (joy1Btn(1)) {
			if (!pressed1) {
				if (!armServoDown) {
					armServoDown = true;
					servo[armHatch] = ARMHATCHUP;
				}
				else {
					armServoDown = false;
					servo[armHatch] = ARMHATCHDOWN;
				}
			}
			pressed1 = true;
		}
		else {
			pressed1 = false;
		}

		//Left and right cylinder collectors
		if (joy1Btn(3)) {
			if (!pressed3) {
				if (!grabCylinderDown) {
					grabCylinderDown = true;
					setGrabberServo(GRABBERDOWN);
				}
				else {
					grabCylinderDown = false;
					setGrabberServo(GRABBERUP);
				}
			}
			pressed3 = true;
		}
		else {
			pressed3 = false;
		}

		//collector intake/outake
		if (joy1Btn(2))
			motor[collector] = 100;
		else if (joy1Btn(4))
			motor[collector] = -100;
		else
			motor[collector] = 0;


	}
}



task main()
{
	//Initialize components
	setMotor(0, 0, 0, 0);

	setGrabberServo(GRABBERUP);

	servo[armHatch] = ARMHATCHUP;

	//Wait for Comp. to start
#ifdef wait_for_start
	waitForStart();
#endif

	StartTask(btnListener);

	//Main loop
	while(true) {
		getJoystickSettings(joystick);

		/////////////////////////////JOYSTICK MOVEMENT//////////////////////////

		//Vals on joy1_x/y1 will be translatational
		float tX = joystick.joy1_x1;
		float tY = joystick.joy1_y1;

		//Val on joy1_x2 will be for x rotation
		float rotX = joystick.joy1_x2;


		//Var to hold motor vals
		mVals *m;
		//clearDebugStream();

		//No unwanted movement
		m = (abs(tX) > THRESHOLD || abs(tY) > THRESHOLD || abs(rotX) > THRESHOLD) ? calcJoyMove(tX, tY, rotX) : setMVals(0, 0, 0, 0);

		////////////////////////D PAD MOVEMENT//////////////////////////////
		//D Pad will always override joystick
		//Check to see if top hat isnt centered (centered yields -1
		if (joystick.joy1_TopHat != -1) {

			switch (joystick.joy1_TopHat) {
			case 0: //Top
				m = forward(100);
				break;

			case 1: //Top Right
				m = diagFR(100);
				break;

			case 2: //Right
				m = strafeR(100);
				break;

			case 3: //Bottom Right
				m = diagBR(100);
				break;

			case 4: //Bottom
				m = backward(100);
				break;

			case 5: //Bottom Left
				m = diagBL(100);
				break;

			case 6: //Left
				m = strafeL(100);
				break;

			case 7: //Top Left
				m = diagFL(100);
				break;

			default:
				break;
			}
		}

		setMotor(m);

	}

}

/* Functions */

//ELevator Movements
void moveElevatorDown() {
	elevatorMoving = true;
	while(!TSreadState(elevatorTouch)) {
		motor[elevator] = -100;
	}

	motor[elevator] = 0;
	elevatorMoving = false;
}

void setGrabberServo(int val) {
	servo[lServo] = val;
	servo[rServo] = 255-val;
}

void setMotor(mVals *m) {
	//Pass along to func with the four motor vals
	setMotor(m->fl, m->fr, m->bl, m->br);
}

void setMotor(float fl, float fr, float bl, float br) {
	motor[FL] = fl;
	motor[FR] = fr;
	motor[BL] = bl;
	motor[BR] = br;
}
